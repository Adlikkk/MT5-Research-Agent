"""Phase 5 EA Lab.

Create, compile, smoke-test, version, and improve MetaTrader 5 Expert Advisors.

Hard rules:
- Generated EAs default to safe behavior: one position at a time, no martingale,
  no grid, no unlimited stacking, explicit risk inputs, a session filter, and a
  spread filter. Every meaningful value is an MQL5 ``input``.
- Generated EAs never contain hidden network calls or hardcoded account
  credentials.
- A working EA version is never overwritten without keeping the previous source
  in ``versions/``. Every change records the reason and supporting evidence.
- The Lab only ever drives the Strategy Tester. It never places live orders.
"""

from __future__ import annotations

